# Challenge 2: Math Operators
# Practice using arithmetic operators (+, -, *, /, //, %, **)
# Math Operators
x = 10
y = 3

# Addition
print(f"Addition: {x + y}")  # Output: 13

# Subtraction
print(f"Subtraction: {x - y}")  # Output: 7

# Multiplication
print(f"Multiplication: {x * y}")  # Output: 30

# Division
print(f"Division: {x / y}")  # Output: 3.333...

# Floor Division
print(f"Floor Division: {x // y}")  # Output: 3

# Modulus
print(f"Modulus: {x % y}")  # Output: 1

# Exponentiation
print(f"Exponentiation: {x ** y}")  # Output: 1000

# Assignment Operators
x=x+2
print(f"x=x+2: {x}")#12
x+=2
print(f"x+=2: {x}")#14
