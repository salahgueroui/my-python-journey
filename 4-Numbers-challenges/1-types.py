# Challenge 1: Types
# Practice working with different number types in Python (int, float, complex)
# Numbers Types
x=10
y=10.5
z=2+3j

# print numbers types
# int
print(type(x))
# float
print(type(y))
# complex
print(type(z))

# String Type
a="10"
# print string type
print(type(a))
# print string
print(a*2)

# convert string to int
a=int(a)
print(type(a))
print(a*2)

b="10.5"
# convert string to float
print(float(b))

# Convert two integers to a complex number
c = 3  # real part
d = 4  # imaginary part

print(complex(c, d))  # Output: (3+4j)